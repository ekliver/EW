package sys.model;

public class AimarConductores {
private String dniCon;
private String abrCon;
private String desCon;
private String breCon;
private String notCon;

    public String getDniCon() {
        return dniCon;
    }

    public void setDniCon(String dniCon) {
        this.dniCon = dniCon;
    }

    public String getAbrCon() {
        return abrCon;
    }

    public void setAbrCon(String abrCon) {
        this.abrCon = abrCon;
    }

    public String getDesCon() {
        return desCon;
    }

    public void setDesCon(String desCon) {
        this.desCon = desCon;
    }

    public String getBreCon() {
        return breCon;
    }

    public void setBreCon(String breCon) {
        this.breCon = breCon;
    }

    public String getNotCon() {
        return notCon;
    }

    public void setNotCon(String notCon) {
        this.notCon = notCon;
    }

}
